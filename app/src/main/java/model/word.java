package model;

public class word {
    public String mword;
    public String eword;
    public int itemid;

    public String getMword() {
        return mword;
    }

    public void setMword(String mword) {
        this.mword = mword;
    }

    public String getEword() {
        return eword;
    }

    public void setEword(String eword) {
        this.eword = eword;
    }

    public int getItemid() {
        return itemid;
    }

    public void setItemid(int itemid) {
        this.itemid = itemid;
    }
}
